a = 10
b = 3
print("Addition: ",a+b)
print("Subtraction: ",a-b)
print("Multiplication: ",a*b)
print("Division: ",a/b) #this is the division operator it always return float value 
print("Floor Division: ",a//b) #this (floor division) operator removes decimal part and displays only the integer value
print("Moduus: ",a%b)  #this is the modulus operator which show the remainder after division
print("Power: ",a**b) # this is the power operator. here is works as a^b